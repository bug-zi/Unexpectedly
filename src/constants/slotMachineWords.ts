/**
 * 检查是否触发特殊组合
 */
export function checkSpecialCombination(
  words: [string, string, string]
): typeof SPECIAL_COMBINATIONS[number] | undefined {
  return SPECIAL_COMBINATIONS.find((combo) => {
    const sortedWords = [...words].sort();
    const sortedCombo = [...combo.words].sort();
    return (
      sortedWords[0] === sortedCombo[0] &&
      sortedWords[1] === sortedCombo[1] &&
      sortedWords[2] === sortedCombo[2]
    );
  });
}

/**
 * 检查是否三连相同
 */
export function checkTriple(words: [string, string, string]): boolean {
  return words[0] === words[1] && words[1] === words[2];
}
